
public class JavaSwitch {

	public static void main(String[] args) {
		
		int a=1;
		switch(a*2){
			
			case 0:
			System.out.println("Inside case 0");
			break;
			case 1:
				System.out.println("Inside case 1");
			break;
			case 2:
				System.out.println("Inside case 2");
			break;
			default:
			System.out.println("Inside default case");
			

	}
	}
}
